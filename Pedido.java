    public void envia_dados() {
        if (nova == null) {
            nova = new Senha_gerada();
            nova.setVisible(true);
            nova.recebe_senha(pedido.getSenha());
            nova.recebe_depto(pedido.getDepartamento());
        } else {
            nova.setVisible(true);
            nova.setState(Senha_gerada.NORMAL);
            nova.recebe_senha(pedido.getSenha());
            nova.recebe_depto(pedido.getDepartamento());
        }
    }

