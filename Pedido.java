    private void envia_central() {
        try {
            socket = new Socket("localhost", 2800);
            ObjectOutputStream requestObjectToServer = new ObjectOutputStream(socket.getOutputStream());
            requestObjectToServer.writeObject(pedido);
            
            ObjectInputStream replyObjectFromServer = new ObjectInputStream(socket.getInputStream());
            pedido = (Pedido_Senha) replyObjectFromServer.readObject();
            //jTextField3.setText(pedido.isConfirma_envio());
            
            socket.close();
            
        } catch (IOException ex) {
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Pedido.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

